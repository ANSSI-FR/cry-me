#include <string.h>
#include "utilities.h"

size_t check_eq_cst(uint8_t const * a, uint8_t const * b, size_t len){
    size_t res = 0;
    for(size_t i=0; i<len; i++){
        res |= (a[i] ^ b[i]);
    }

    return !res;
}
